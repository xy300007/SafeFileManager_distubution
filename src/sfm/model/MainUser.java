package sfm.model;

public class MainUser {
	private String userName = null;
	private String userPassword = null;
	private String userNameFinalString = null;
	private String userPasswordFinalString = null;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserNameFinalString() {
		return userNameFinalString;
	}
	public void setUserNameFinalString(String userNameFinalString) {
		this.userNameFinalString = userNameFinalString;
	}
	public String getUserPasswordFinalString() {
		return userPasswordFinalString;
	}
	public void setUserPasswordFinalString(String userPasswordFinalString) {
		this.userPasswordFinalString = userPasswordFinalString;
	}

}
